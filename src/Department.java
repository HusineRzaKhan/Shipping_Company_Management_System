public class Department {
    private String name;
    private int depid;
    
    public Department(String name, int depid) {
        this.name = name;
        this.depid = depid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDepid() {
        return depid;
    }

    public void setDepid(int depid) {
        this.depid = depid;
    }

    

}
